    public boolean check() {
        if (txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên");
            txtName.requestFocus();
            return false;
        }
        if (txtAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập địa chỉ");
            txtAddress.requestFocus();
            return false;
        }
        if (!radbntMale.isSelected() && !radbntFemale.isSelected()) {
            JOptionPane.showMessageDialog(this, "Chưa chọn giới tính");
            return false;
        }
        if (!ckbReading.isSelected() && !ckbSinging.isSelected() && !ckbDancing.isSelected()) {
            JOptionPane.showMessageDialog(this, "Chưa chọn sở thích");
        }
        return true;
    }


