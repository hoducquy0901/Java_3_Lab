    private void bntValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntValidateActionPerformed
        // TODO add your handling code here:
        if (check()) {
            String str = "";
            str = "Name: " + txtName.getText() + "\n";
            str = str + "Address: " + txtAddress.getText() + "\n";
            if (radbntMale.isSelected()) {
                str = str + "Sex: Male\n";
            } else {
                str = str + "Sex: Female\n";
            }
            if (cbbQualification.getSelectedIndex() == 0) {
                str = str + "Qualification: Graduate\n";
            } else if (cbbQualification.getSelectedIndex() == 1) {
                str = str + "Qualification: Student\n";
            }
            if (ckbDancing.isSelected()) {
                str = str + "Hobby: Dancing\n";
            }
            if (ckbReading.isSelected()) {
                str = str + "Hobby: Reading\n";
            }
            if (ckbSinging.isSelected()) {
                str = str + "Hobby: Singing\n";
            }
            JOptionPane.showMessageDialog(this, str);
        }
    }//GEN-LAST:event_bntValidateActionPerformed

